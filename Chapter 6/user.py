user_0 = {
    'username': 'SelfHostedToast',
    'first': 'Dominick',
    'last': 'Smith',
}

# Using the items() method (returns the key, value pair of a dictionary) we loop through the dictionary, assigning each pair to its respective key or value and printing them as such.

for key, value in user_0.items():
    print(f"\nKey: {key}")
    print(f"Value: {value}")